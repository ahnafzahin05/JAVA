package Class_09_Strings;

public class Plus {
    public static void main(String[] args) {
        String s = "Erasmus";
        s+= " mundus" + " 2029" + "\n" + "In Sha Allah";
        System.out.println(s);
        System.out.println(10+20);
        System.out.println("zahin"+10+20);
        System.out.println("zahin"+(10+20));
        System.out.println(10+20+"zahin");
        System.out.println(10+"zahin"+20);
        System.out.println('A' + "zahin");
        System.out.println(100+'A'+"zahin");
        System.out.println('A'+100+"zahin");
    }
}
