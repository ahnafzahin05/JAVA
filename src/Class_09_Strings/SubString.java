package Class_09_Strings;

public class SubString {
    public static void main(String[] args) {
        //substring--> continuous part of string
        String s = "Bangladesh";
        System.out.println(s.substring(3)); //print from index 3 to rest
        System.out.println(s.substring(1,6)); //prints from i to (j-1) i.e. index 1 to 5 here
    }
}
